import { listTriggers } from './manager.ts';
import { makeExecutable } from '../../../../src/utils/permissions.ts';

let serverPort = 3000;

export function setGitHookServerPort(port: number): void {
  serverPort = port;
}

export async function installGitHooks(repoPath: string): Promise<string[]> {
  const installed: string[] = [];
  const triggers = listTriggers().filter(
    (t) => t.enabled && t.source === 'git_hook' && t.gitHook?.repoPath === repoPath,
  );

  for (const trigger of triggers) {
    if (!trigger.gitHook) continue;

    for (const hookName of trigger.gitHook.hooks) {
      const hookPath = `${repoPath}/hooks/${hookName}`;
      const timestamp = new Date().toISOString();

      const script = `#!/bin/sh
# Cortex git hook: ${trigger.name}
# Generated by Cortex on ${timestamp}

HOOK_NAME="${hookName}"
REPO_PATH="${repoPath}"

PAYLOAD=$(cat <<EOF
{
  "event": "${hookName}",
  "repo": "${repoPath}",
  "timestamp": "${timestamp}",
  "branch": "$(git -C "${repoPath}" rev-parse --abbrev-ref HEAD 2>/dev/null || echo unknown)",
  "commit": "$(git -C "${repoPath}" rev-parse HEAD 2>/dev/null || echo unknown)",
  "message": "$(git -C "${repoPath}" log -1 --format=%s 2>/dev/null || echo unknown)"
}
EOF
)

CORTEX_PORT=\${CORTEX_PORT:-${serverPort}}

curl -s -X POST "http://localhost:\${CORTEX_PORT}/api/webhooks/${trigger.name}" \\
  -H "Content-Type: application/json" \\
  -H "X-Event-Type: git-${hookName}" \\
  -d "$PAYLOAD"

exit 0
`;

      try {
        await Deno.writeTextFile(hookPath, script);
        await makeExecutable(hookPath);
        installed.push(hookName);
        console.log(`[triggers:git] Installed ${hookName} hook for ${trigger.name} at ${hookPath}`);
      } catch (e) {
        console.error(`[triggers:git] Failed to install ${hookName} hook: ${(e as Error).message}`);
      }
    }
  }

  return installed;
}

export async function uninstallGitHooks(repoPath: string): Promise<string[]> {
  const removed: string[] = [];
  const triggers = listTriggers().filter(
    (t) => t.source === 'git_hook' && t.gitHook?.repoPath === repoPath,
  );

  for (const trigger of triggers) {
    if (!trigger.gitHook) continue;

    for (const hookName of trigger.gitHook.hooks) {
      const hookPath = `${repoPath}/hooks/${hookName}`;
      try {
        const content = await Deno.readTextFile(hookPath);
        if (content.includes('# Cortex git hook:')) {
          await Deno.remove(hookPath);
          removed.push(hookName);
        }
      } catch {
        // file doesn't exist
      }
    }
  }

  return removed;
}
